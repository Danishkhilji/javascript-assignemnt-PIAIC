var animals = ["Goat","Cow","Hourse"]



for(var i=0; i<=animals.length; i++){
    if(animals[i]=="Goat"){
        console.log(`Baby ${animals[i]} looks really cute`);
    }
    if(animals[i]=="Cow"){
        console.log(`${animals[i]} milk is a good source of protein and calcium`);
    }
    if(animals[i]=="Hourse"){
        console.log(`${animals[i]} run very fast`);
    }
}

console.log("All of these animals are mammals");